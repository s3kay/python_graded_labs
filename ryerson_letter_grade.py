def ryerson_letter_grade(pct):

    if pct >= 90:
        return('A+')
    elif pct >= 85:
        return ('A')
    elif pct >= 80:
        return ('A-')
    elif pct >= 77:
        return ('B+')
    elif pct >= 73:
        return ('B')
    elif pct >= 70:
        return ('B-')
    elif pct >= 67:
        return ('C+')
    elif pct >= 63:
        return ('C')
    elif pct >= 60:
        return ('C-')
    elif pct >= 57:
        return ('D+')
    elif pct >= 53:
        return ('D')
    elif pct >= 50:
        return ('D-')
    elif pct >= 0:
        return ('F')
