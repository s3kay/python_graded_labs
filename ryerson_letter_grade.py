def ryerson_letter_grade(pct):

    if pct >= 90:
        print('A+')
    elif pct >= 85:
        print ('A')
    elif pct >= 80:
        print ('A-')
    elif pct >= 77:
        print ('B+')
    elif pct >= 73:
        print ('B')
    elif pct >= 70:
        print ('B-')
    elif pct >= 67:
        print ('C+')
    elif pct >= 63:
        print ('C')
    elif pct >= 60:
        print ('C-')
    elif pct >= 57:
        print ('D+')
    elif pct >= 53:
        print ('D')
    elif pct >= 50:
        print ('D-')
    elif pct >= 0:
        print ('F')